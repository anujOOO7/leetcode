class Solution {
public:
    int solve(int a, int b, int c) {
        // code here
        int l=max(a,max(b,c));
        int s=min(a,min(b,c));
        int m=a+b+c-(l+s);
        if(l>2*(s+m+1)) return -1;
        return a+b+c;
    }
};

