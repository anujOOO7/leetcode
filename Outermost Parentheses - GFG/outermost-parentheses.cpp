class Solution {
  public:
    string removeOuter(string& s) {
        // code here
        string ans;
        int count=0;
        
        for(auto ch:s){
            if(ch=='(' && count++>0)
                ans+=ch;
            if(ch==')' && count-->1)
                ans+=ch;
        }
        return ans;
    }
};

